#include <stdio.h>

/**
 * add -sum of two integers
 * return sum
**/

int add(int a , int b)
{
	return(a + b);
}

/**
 * sub -subtract two numbers
 * return subtract
**/

int sub(int a , int b)
{
	return(a - b);
}

/**
 * mul -multiply two numbers
 * return mul
**/ 

int mul(int a , int b)
{
	return(a * b);
}

/**
 * div -divide two numbers
 * return div
**/

int div(int a , int b)
{
	return(a / b);
}

/**
 * mod -modulus of two numbers
 * return mod
**/

int mod(int a , int b)
{
	return(a % b);
}	
